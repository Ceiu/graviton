package com.redhat.graviton.api.poc.model;





 public class ExtAttribute {

    private String name;
    private String value;

    public String getName() {
      return this.name;
   }

   public ExtAttribute setName(String name) {
      this.name = name;
      return this;
   }

    public String getValue() {
      return this.value;
   }

   public ExtAttribute setValue(String value) {
      this.value = value;
      return this;
   }

}
